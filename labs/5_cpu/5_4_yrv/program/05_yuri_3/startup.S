################################################################################
# RISC-V MACHINE-MODE INTERRUPT REGISTERS REFERENCE
#
# mstatus (Machine Status) - Global control and state storage
#   - mstatus.MIE  [Bit 3]  : Global Interrupt Enable (1 = ON, 0 = OFF)
#   - mstatus.MPIE [Bit 7]  : Previous MIE value (saved here on trap)
#   - mstatus.MPP  [Bits 11:12] : Previous Privilege Mode (saved here on trap)
#
# mie (Machine Interrupt Enable) - Fine-grained interrupt masking
#   - mie.MEIE     [Bit 11] : Machine External Interrupt Enable (Peripherals)
#   - mie.MTIE     [Bit 7]  : Machine Timer Interrupt Enable
#   - mie.MSIE     [Bit 3]  : Machine Software Interrupt Enable
#
# mip (Machine Interrupt Pending) - Status flags of raised interrupts
#   - mip.MEIP     [Bit 11] : Machine External Interrupt Pending status
#   - mip.MTIP     [Bit 7]  : Machine Timer Interrupt Pending status
#   - mip.MSIP     [Bit 3]  : Machine Software Interrupt Pending status
#
# mcause (Machine Cause) - Reflects the reason for entering the trap
#   - mcause.INTERRUPT [Bit 31]  : 1 = Asynchronous Interrupt, 0 = Synchronous Exception
#   - mcause.EXCCODE   [Bits 30:0]: Event code ID (e.g., 0xB for External Interrupt)
#
# mtvec (Machine Trap Vector) - Configures trap handler entry addresses
#   - mtvec.BASE   [Bits 31:2] : Base address of the trap handler
#   - mtvec.MODE   [Bits 1:0]  : 00 = Direct Mode (single address for all traps)
#                                01 = Vectored Mode (BASE + 4 * EXCCODE)
#
# mepc (Machine Exception Program Counter)
#   - Holds the PC value of the interrupted instruction. Used by 'mret' to return.
#
# mscratch (Machine Scratch)
#   - Dedicated context register used to temporarily swap and save CPU registers.
################################################################################

.equ  RISCV_MSTATUS_MIE, 0x08

.equ  iobase,            0xffff0     # i/o at 0xffff0000

.section text.zero
# ==============================================================================
# SECTION: Goto to init
# ==============================================================================
# Some soft cores start from 0  
    j __RST_BASE



# ==============================================================================
# SECTION: NMI HANDLER 0x100
# ==============================================================================
# NMI Trap handler

.section .text.nmi_vec
.global nmi_vec

nmi_vec:
    mret


# ==============================================================================
# SECTION: DBG HANDLER 0x140
# ==============================================================================
.section .text.dbg_vec
.global dbg_vec

dbg_vec:
    dret


# ==============================================================================
# SECTION: DEX HANDLER 0x1c0
# ==============================================================================
.section .text.dex_vec
.global dex_vec
dex_vec:
    dret


# ==============================================================================
# SECTION: RESET (main) 0x200
# ==============================================================================

.section .text.init
.global _start

_start:
loop:

# ------------------------------------------------------------------------------
# Sub-section: Interrupt handler initialization
# ------------------------------------------------------------------------------
    la      t0, __TRAP_ACK        # CPU cores can use different default trap vector
                                # Use TRAP_ACK address from lionker script
    andi    t0, t0, ~3          # Select Direct mode for interrupt handling 
    csrw    mtvec, t0           # Set TRAP_ACK as interrupt handler address

# ------------------------------------------------------------------------------
# Sub-section: mscratch initialization
# ------------------------------------------------------------------------------
# used trapframe scetion with 128 bytes lenght

    csrc    mstatus, RISCV_MSTATUS_MIE     # Clear MIE bit to disable all interrupts globally
    la      t0, __trapframe_start
    csrw    mscratch, t0

# ------------------------------------------------------------------------------
# Sub-section: Zero BSSs
# ------------------------------------------------------------------------------
# https://eseo-tech.github.io/emulsiV/doc/
# https://github.com/riscv-mcu/GD32VF103_Firmware_Library/blob/master/Firmware/RISCV/env_Eclipse/start.S

# Load the memory boundaries generated by the linker script
    la      t0, __sbss_start    # Start address of memory to clear (from .sbss section)
    la      t1, __bss_end       # End address of memory to clear (from .bss section)

    # Check if the BSS sections are empty (if t0 >= t1, skip the loop entirely)
    bgeu    t0, t1, bss_clr_done

bss_clr:
    sw      zero, 0(t0)         # Store a 32-bit zero into the memory address held in t0
    addi    t0, t0, 4           # Advance the memory pointer by 4 bytes (1 word)
    bltu    t0, t1, bss_clr     # If t0 is still less than t1, continue clearing memory

bss_clr_done:


# Inform the debugger that this is the root of the call stack and there are no callers above.
# https://sourceware.org/binutils/docs/as/CFI-directives.html
# ------------------------------------------------------------------------------
# Sub-section: startproc
# ------------------------------------------------------------------------------

.cfi_startproc
.cfi_undefined ra

# .option push     - Save the current assembler options configuration.
# .option norelax  - Disable linker relaxation to prevent relaxation of the GP initialization.
# .option pop      - Restore the previously saved assembler options configuration.
# https://sourceware.org/binutils/docs/as/RISC_002dV_002dDirectives.html
# https://github.com/riscvarchive/riscv-newlib/blob/riscv-newlib-3.2.0/libgloss/riscv/crt0.S


# https://twilco.github.io/riscv-from-scratch/2019/04/27/riscv-from-scratch-2.html
.option push
.option norelax
	la gp, __global_pointer$
.option pop

	la sp, __stack_top
	add s0, sp, zero     #Frame pointer is initialized to the top of the stack.

	jal zero, main
.cfi_endproc

    beq   zero, zero, loop

# ==============================================================================
# END OF PROGRAM
# ==============================================================================
